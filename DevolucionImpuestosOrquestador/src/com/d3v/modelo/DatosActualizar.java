
package com.d3v.modelo;

public class DatosActualizar {

    private String estado;
    private String claveAccesoComprobante;
    private String mensajeSolicitud;

    public DatosActualizar() {
        super();
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getClaveAccesoComprobante() {
        return claveAccesoComprobante;
    }

    public void setClaveAccesoComprobante(String claveAccesoComprobante) {
        this.claveAccesoComprobante = claveAccesoComprobante;
    }

    public String getMensajeSolicitud() {
        return mensajeSolicitud;
    }

    public void setMensajeSolicitud(String mensajeSolicitud) {
        this.mensajeSolicitud = mensajeSolicitud;
    }
    
    
    
}
